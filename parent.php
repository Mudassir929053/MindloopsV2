    <script>
        function toggleStyle(button) {
            // Remove active styles from all buttons
            const buttons = document.querySelectorAll(".nav-item button");
            buttons.forEach((btn) => {
                btn.classList.remove("active");
                buttons.forEach(btn => btn.classList.remove("btn-warning"));
                btn.style.backgroundColor = "";
                btn.style.color = "";

            });

            // Apply active styles to the clicked button
            button.classList.add("active");
            button.style.backgroundColor = "#FCA600";
            button.style.color = "#000000";
            button.style.border = "none";
        }
    </script>
